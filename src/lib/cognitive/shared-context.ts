export type LearningContext = Record<string, unknown>;

// ── Metadata (owner: system — managed by Context Manager) ───────────────

export interface ContextMetadata {
  /** Schema version (bump when the Shared Context shape changes materially). */
  schemaVersion: number;
  /** Monotonically increasing version within a request lifecycle. */
  version: number;
  /** Correlation ids for tracing. */
  correlation: CorrelationIds;
  /** Full provenance audit trail. */
  provenance: ProvenanceEntry[];
  /** Overall confidence in the context (0-1), derived from section confidences. */
  confidence: number;
  createdAt: string;
  updatedAt: string;
  /** Whether the context has been frozen (read-only terminal state). */
  frozen: boolean;
}

// ── The Shared Context Object ────────────────────────────────────────────

/**
 * The Shared Context Object — the single source of truth that flows through
 * the cognitive pipeline.
 *
 * Ownership map:
 *   metadata      → system (Context Manager)
 *   request       → system
 *   session       → system
 *   geographic    → gcie
 *   user          → pmb
 *   reasoning     → crie
 *   validation    → cross-eval
 *   recommendation→ irde
 *   platform      → uob (reserved)
 *   execution     → execution (reserved)
 *   learning      → learning (reserved)
 *   extensions    → any (future-proof escape hatch)
 */
export interface SharedContext {
  metadata: ContextMetadata;
  request: RequestContext;
  session?: SessionContext;
  geographic?: GeographicContext;
  user?: UserContext;
  reasoning?: ReasoningContext;
  validation?: ValidationContext;
  recommendation?: RecommendationContext;
  platform?: PlatformContext;
  execution?: ExecutionContext;
  learning?: LearningContext;
  /**
   * Escape hatch for future sections without schema changes.
   * Use namespaced keys: `{ "custom.feature.x": ... }`.
   */
  extensions?: Record<string, unknown>;
}

