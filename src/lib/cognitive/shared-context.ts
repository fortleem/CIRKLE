// ── Validation ───────────────────────────────────────────────────────────

export interface ContextValidationResult {
  valid: boolean;
  errors: string[];
  warnings: string[];
}

/**
 * Validate a Shared Context against the schema rules.
 * This is a lightweight structural check — it does NOT reason about content.
 *
 * Rules enforced:
 *   - metadata must exist with version >= 1
 *   - request.originalRequest must be a non-empty string
 *   - every populated section must match its owner's type shape (shallow)
 *   - frozen contexts must not be mutated (caller responsibility; flagged here)
 */
export function validateContext(ctx: SharedContext): ContextValidationResult {
  const errors: string[] = [];
  const warnings: string[] = [];

  if (!ctx.metadata) {
    errors.push("metadata is required");
  } else {
    if (typeof ctx.metadata.version !== "number" || ctx.metadata.version < 1) {
      errors.push("metadata.version must be a positive number");
    }
    if (typeof ctx.metadata.schemaVersion !== "number") {
      errors.push("metadata.schemaVersion is required");
    }
    if (!ctx.metadata.correlation?.requestId) {
      errors.push("metadata.correlation.requestId is required");
    }
    if (!Array.isArray(ctx.metadata.provenance)) {
      errors.push("metadata.provenance must be an array");
    }
  }

  if (!ctx.request) {
    errors.push("request section is required");
  } else {
    if (typeof ctx.request.originalRequest !== "string" || ctx.request.originalRequest.length === 0) {
      errors.push("request.originalRequest must be a non-empty string");
    }
    if (typeof ctx.request.language !== "string") {
      warnings.push("request.language is recommended");
    }
  }

  // Shallow presence checks for owned sections (no deep schema enforcement —
  // phases own their section shapes; the manager only enforces ownership).
  if (ctx.geographic && typeof ctx.geographic !== "object") {
    errors.push("geographic section must be an object");
  }
  if (ctx.user && typeof ctx.user !== "object") {
    errors.push("user section must be an object");
  }
  if (ctx.reasoning && typeof ctx.reasoning !== "object") {
    errors.push("reasoning section must be an object");
  }

  return { valid: errors.length === 0, errors, warnings };
}

